export const SpiceIcon = ({ size = 16, level = 'mild' }) => {
  // Renders different amounts or shapes of clean flame/spice vectors depending on level
  let color = 'var(--spice-mild)';
  let flameCount = 1;
  
  if (level === 'medium') {
    color = 'var(--spice-medium)';
    flameCount = 2;
  } else if (level === 'hot') {
    color = 'var(--spice-hot)';
    flameCount = 3;
  } else if (level === 'insane') {
    color = 'var(--spice-insane)';
    flameCount = 4;
  }

  const FlameSVG = () => (
    <svg width={size} height={size} viewBox="0 0 24 24" fill="currentColor" stroke="none" style={{ display: 'inline-block', color }}>
      <path d="M8.5 14.5A2.5 2.5 0 0 0 11 12c0-1.38-.5-2-1-3-1.072-2.143-.224-4.054 2-6 .5 2.5 2 4.9 4 6.5 2 1.6 3 3.5 3 5.5a7 7 0 1 1-14 0c0-1.153.433-2.294 1-3a2.5 2.5 0 0 0 2.5 2.5z" />
    </svg>
  );

  return (
    <div style={{ display: 'inline-flex', gap: '1px', alignItems: 'center' }}>
      {Array.from({ length: flameCount }).map((_, i) => <FlameSVG key={i} />)}
    </div>
  );
};

